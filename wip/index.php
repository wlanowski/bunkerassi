                <div class="x_content">
                    <h4>Körper (KÖR)</h4>
                    Das Attribut beschreibt physische
                    Fitness, Widerstand gegen
                    Degeneration, Wunden, Strahlung,
                    Gifte und Krankheiten.
                    <b>Eigenschaften:</b> Stärke und Härte

                    <h4>Agilität (AGI)</h4>
                    Athletik, Schnelligkeit, Fingerspitzengefühl
                    und Koordinationsvermögen
                    fallen unter dieses Attribut.
                    <b>Eigenschaften:</b> Bewegung und
                    Geschick
                    <h4>Geist (GEI)</h4>
                    Intelligenz und Wissen, aber auch
                    Charisma und der Einsatz von
                    paramentalen Fertigkeiten sind
                    abhängig von diesem Attribut.
                    <b>Eigenschaften:</b> Verstand und Aura

                    <h4>Stärke (ST)</h4>
                    Stärke ist die rohe Körperkraft.
                    Nahkampfangriffe sind von dieser
                    Eigenschaft abhängig.
                    <h4>Härte (HÄ)</h4>
                    Die physische Resistenz gegen
                    Patronen, Pfeile, Schrottteile,
                    Granaten, Gift, Krankheit und
                    natürlich Strahlung. Die Härte
                    bestimmt auch das Ausmaß an
                    kybernetischen Bauteilen, die sich der
                    Charakter implantieren lassen kann.
                    <h4>Bewegung (BE)</h4>
                    Reflexe, Bewegungsradius und
                    Schnelligkeit fallen unter diese
                    Eigenschaft.
                    <h4>Geschick (GE)</h4>
                    Präzision, Koordination. Geschick ist
                    vor allem für den Fernkampf essentiell,
                    jedoch auch für Reparaturen, das
                    Basteln von improvisierten Waffen
                    und die Instandsetzung von alten
                    Computern und anderem Techkram
                    notwendig.

                    <h4>Verstand (VE)</h4>
                    Verstand beinhaltet Intelligenz
                    und Auffassungsgabe, Wissen und
                    Kombinationsvermögen.
                    Für paramentale Charaktere wie
                    Telepathen bestimmt diese Eigenschaft
                    die Macht ihrer Fähigkeiten.
                    Verstand bedingt unter anderem das
                    Techlevel, welches der Charakter
                    verstehen kann.
                    <h4>Aura (AU)</h4>
                    Charisma, Auftreten und
                    Persönlichkeitsstärke sind wichtig
                    für Interaktionen, aber auch für
                    paramentale Fähigkeiten.
